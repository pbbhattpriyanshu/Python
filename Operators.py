# What is Operators?
# Operators are special symbols that perform operations on variables and values.

# Arithmetic Operators
# Arithmetic operators are used to perform mathematical operations such as addition, subtraction, multiplication, and division. The following are the arithmetic operators in Python:
#
# 1. Addition (+): Adds two numbers together.
# 2. Subtraction (-): Subtracts one number from another. 
# 3. Multiplication (*): Multiplies two numbers together.
# 4. Division (/): Divides one number by another and returns a float.
# 5. Modulus (%): Returns the remainder of a division operation.

# Example:

a = 10
b = 3

print("Addition:", a + b)  # Output: 13
print("Subtraction:", a - b)  # Output: 7
print("Multiplication:", a * b)  # Output: 30
print("Division:", a / b)  # Output: 3.3333333333333335
print("Modulus:", a % b)  # Output: 1

# Relational Operators
# Relational operators are used to compare two values. The following are the relational operators in Python:

# 1. Equal to (==): Returns True if both values are equal.
# 2. Not equal to (!=): Returns True if both values are not equal.
# 3. Greater than (>): Returns True if the left value is greater than the right value.
# 4. Less than (<): Returns True if the left value is less than the right value.
# 5. Greater than or equal to (>=): Returns True if the left value is greater than or equal to the right value.
# 6. Less than or equal to (<=): Returns True if the left value is less than or equal to the right value.   

# Example:

print("Equal to:", a == b)  # Output: False
print("Not equal to:", a != b)  # Output: True  
print("Greater than:", a > b)  # Output: True
print("Less than:", a < b)  # Output: False
print("Greater than or equal to:", a >= b)  # Output: True
print("Less than or equal to:", a <= b)  # Output: False


# Assignment Operators
# Assignment operators are used to assign values to variables. The following are the assignment operators in Python:

# 1. Assignment (=): Assigns the right value to the left variable.
# 2. Add and assign (+=): Adds the right value to the left variable and assigns the result to the left variable.
# 3. Subtract and assign (-=): Subtracts the right value from the left variable and assigns the result to the left variable.
# 4. Multiply and assign (*=): Multiplies the left variable by the right value and assigns the result to the left variable.
# 5. Divide and assign (/=): Divides the left variable by the right value and assigns the result to the left variable.
# 6. Modulus and assign (%=): Takes the modulus of the left variable by the right value and assigns the result to the left variable.
# 7. Floor division and assign (//=): Divides the left variable by the right value and assigns the result to the left variable as an integer.
# 8. Exponentiation and assign (**=): Raises the left variable to the power of the right value and assigns the result to the left variable.
# 9. Bitwise AND and assign (&=): Performs a bitwise AND operation on the left variable and the right value and assigns the result to the left variable.
# 10. Bitwise OR and assign (|=): Performs a bitwise OR operation on the left variable and the right value and assigns the result to the left variable.

# Logic Operators
# Logic operators are used to combine conditional statements. The following are the logic operators in Python:
#
# 1. AND: Returns True if both statements are true. 
# 2. OR: Returns True if at least one statement is true.
# 3. NOT: Returns True if the statement is false.
